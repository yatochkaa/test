def _parse_parts_string(parts_str: str) -> list[tuple[str, str]]:
    result = []
    if not parts_str:
        return result
    for chunk in str(parts_str).split(","):
        chunk = chunk.strip()
        if "|" not in chunk:
            continue
        brand, art = chunk.split("|", 1)
        brand, art = brand.strip(), art.strip()
        if not art or art == "-":
            continue
        if brand.upper() in TRUCK_BRANDS:
            continue
        result.append((brand, art))
    return result


def _collect_part_rows(data) -> list[dict]:
    """Собирает «строки-группы» getPartsbyVIN из разных форм ответа.

    Возвращает список dict-ов вида {group, name, parts, ...}, ещё НЕ
    развёрнутых в отдельные запчасти. Поддерживает:
      • top-level list[dict]
      • dict с ключами items / parts(list) / data / array
      • вложенный data: {items|array|parts: [...]}
      • dict, который сам является строкой-группой (parts/brand/article)
    """
    if isinstance(data, list):
        return [row for row in data if isinstance(row, dict)]

    if not isinstance(data, dict):
        return []

    rows: list[dict] = []
    for key in ("items", "parts", "data", "array"):
        val = data.get(key)
        if isinstance(val, list):
            rows.extend(row for row in val if isinstance(row, dict))

    inner = data.get("data")
    if isinstance(inner, dict):
        for key in ("items", "array", "parts"):
            val = inner.get(key)
            if isinstance(val, list):
                rows.extend(row for row in val if isinstance(row, dict))

    # Если ничего не собрали, но сам dict похож на строку-группу или
    # отдельную запчасть — используем его как единственную строку.
    if not rows and (data.get("parts") or data.get("brand") or data.get("article")):
        rows.append(data)

    return rows


def _iter_brand_article(parts_field) -> list[tuple[str, str]]:
    """Разбирает поле parts в список (brand, article).

    parts может быть:
      • строкой "BRAND|ART,BRAND2|ART2,..."
      • списком dict {brand/manuName, article/number/oem}
      • списком строк "BRAND|ART"
    """
    pairs: list[tuple[str, str]] = []
    if not parts_field:
        return pairs

    if isinstance(parts_field, str):
        for chunk in parts_field.split(","):
            chunk = chunk.strip()
            if "|" not in chunk:
                continue
            brand, art = chunk.split("|", 1)
            pairs.append((brand.strip(), art.strip()))
        return pairs

    if isinstance(parts_field, list):
        for el in parts_field:
            if isinstance(el, dict):
                brand = el.get("brand") or el.get("manuName") or el.get("supplier") or ""
                art = (
                    el.get("article")
                    or el.get("number")
                    or el.get("oem")
                    or el.get("artikul")
                    or ""
                )
                if brand and art:
                    pairs.append((str(brand).strip(), str(art).strip()))
            elif isinstance(el, str) and "|" in el:
                brand, art = el.split("|", 1)
                pairs.append((brand.strip(), art.strip()))

    return pairs


def _extract_items_from_raw(data) -> list[dict]:
    """Разворачивает ответ getPartsbyVIN в ПЛОСКИЙ список запчастей.

    Каждый элемент: {"brand", "article", "name", "group", "shortname"}.
    Грузовые бренды (TRUCK_BRANDS) и пустые артикулы отбрасываются.
    Дедупликация по brand|normalize(article). Для [] / None / неверного
    типа возвращает [] без исключений.
    """
    items: list[dict] = []
    seen: set[str] = set()

    for row in _collect_part_rows(data):
        name = str(row.get("name") or row.get("shortname") or "").strip()
        group = str(row.get("group") or "").strip()
        shortname = str(row.get("shortname") or "").strip()

        pairs = _iter_brand_article(row.get("parts"))

        # row может сам быть отдельной запчастью (без поля parts)
        if not pairs:
            brand = str(row.get("brand") or "").strip()
            article = str(row.get("article") or row.get("number") or "").strip()
            if brand and article:
                pairs = [(brand, article)]

        for brand, article in pairs:
            brand = (brand or "").strip()
            article = (article or "").strip()
            if not brand or not article or article == "-":
                continue
            if brand.upper() in TRUCK_BRANDS:
                continue
            key = f"{brand.upper()}|{normalize_article(article)}"
            if key in seen:
                continue
            seen.add(key)
            items.append({
                "brand": brand,
                "article": article,
                "name": name,
                "group": group,
                "shortname": shortname,
            })

    return items


def _classify_error_code(error_code, message, http_status=None) -> tuple[str, str | None]:
    """Приводит код/сообщение ошибки API к единому виду.

    Возвращает (normalized_error_code, message). Распознаёт:
      • RATE_LIMIT  — error_code 5000 / "Exceeded the number of requests" / HTTP 401|429
      • SERVER_ERROR — error_code 5007 / "Request error" / HTTP >= 500
      • AUTH        — unauthorized / invalid key
    """
    code_str = str(error_code).strip() if error_code not in (None, "") else ""
    msg = str(message or "")
    msg_l = msg.lower()

    try:
        http_int = int(http_status) if http_status is not None else None
    except (TypeError, ValueError):
        http_int = None

    if code_str == "5000" or "exceeded the number of requests" in msg_l or http_int in (401, 429):
        return "RATE_LIMIT", msg or "Превышен лимит запросов к API (rate limit)"

    if code_str == "5007" or "request error" in msg_l or (http_int is not None and http_int >= 500):
        return "SERVER_ERROR", msg or "Ошибка сервера API"

    if "unauthorized" in msg_l or "invalid key" in msg_l or "access denied" in msg_l:
        return "AUTH", msg or "Ошибка авторизации (ключ API)"

    if code_str:
        return code_str, (msg or None)

    return "API_ERROR", (msg or None)


def _detect_api_error_in_body(data) -> tuple[bool, str | None, str | None]:
    if not isinstance(data, dict):
        return False, None, None

    error_code = data.get("error_code")
    message = data.get("message") or data.get("error") or data.get("detail")

    if error_code:
        norm_code, norm_msg = _classify_error_code(error_code, message)
        return True, norm_code, norm_msg

    if isinstance(message, str):
        m = message.lower()
        if "exceeded the number of requests" in m:
            return True, "RATE_LIMIT", message
        if "request error" in m:
            return True, "SERVER_ERROR", message
        if "unauthorized" in m or "invalid key" in m:
            return True, "AUTH", message

    return False, None, message


def normalize_getparts_response(raw, http_status, exception=None, cat="", vin="") -> dict:
    ctx = f"[VIN={vin} cat={cat}]"

    def _make(api_status, items_raw=None, http_st=None, error_code=None, message=None, raw_body=None) -> dict:
        items_raw = items_raw or []
        return {
            "ok": api_status == "OK",
            "is_error": api_status == "ERR",
            "is_empty": api_status == "NO",
            "api_status": api_status,                   # OK | NO | ERR
            "semantic_status": "NOT_APPLICABLE",       # позже обновится фильтром
            "http_status": http_st,
            "status": http_st,
            "error_code": error_code,
            "message": message,
            "total": len(items_raw),
            "items": list(items_raw),
            "items_raw": list(items_raw),
            "total_raw": len(items_raw),
            "raw": raw_body,
        }

    if exception is not None:
        exc_name = type(exception).__name__
        exc_msg = str(exception)
        logger.warning("%s ERR exception=%s: %s", ctx, exc_name, exc_msg)
        return _make(
            "ERR",
            http_st=http_status,
            error_code=exc_name,
            message=exc_msg,
            raw_body=raw,
        )

    if http_status is None or http_status >= 400:
        raw_code = None
        raw_msg = None
        if isinstance(raw, dict):
            raw_code = raw.get("error_code") or raw.get("status")
            raw_msg = raw.get("message") or raw.get("error") or raw.get("description")
        error_code, message = _classify_error_code(raw_code, raw_msg, http_status)
        logger.warning("%s ERR http=%s error_code=%s message=%s", ctx, http_status, error_code, message)
        return _make(
            "ERR",
            http_st=http_status,
            error_code=error_code,
            message=message,
            raw_body=raw,
        )

    api_error, error_code, body_message = _detect_api_error_in_body(raw)
    if api_error:
        logger.warning("%s ERR http=200 body_error=%s message=%s", ctx, error_code, body_message)
        return _make(
            "ERR",
            http_st=http_status,
            error_code=error_code,
            message=body_message,
            raw_body=raw,
        )

    if raw is None:
        logger.warning("%s ERR null body", ctx)
        return _make(
            "ERR",
            http_st=http_status,
            error_code="NULL_BODY",
            message="API returned null body",
            raw_body=None,
        )

    if not isinstance(raw, (list, dict)):
        logger.warning("%s ERR malformed type=%s", ctx, type(raw).__name__)
        return _make(
            "ERR",
            http_st=http_status,
            error_code="MALFORMED",
            message=f"Unexpected type: {type(raw).__name__}",
            raw_body=raw,
        )

    items = _extract_items_from_raw(raw)

    if items:
        logger.info("%s OK total_raw=%d", ctx, len(items))
        return _make(
            "OK",
            items_raw=items,
            http_st=http_status,
            message=body_message,
            raw_body=raw,
        )

    logger.info("%s NO total_raw=0", ctx)
    return _make(
        "NO",
        items_raw=[],
        http_st=http_status,
        message=body_message,
        raw_body=raw,
    )


def _item_text(item: dict) -> str:
    bits = []
    for k in ("name", "group", "shortname", "description", "comment", "brand", "article"):
        v = item.get(k)
        if v:
            bits.append(str(v))
    # обратная совместимость со старым форматом item-ов
    for brand, art in item.get("_parts_parsed", []):
        bits.append(f"{brand} {art}")
    return " ".join(bits).lower()


def filter_items_legacy_by_query_intent(items: list[dict], part_text: str, cat_id: str) -> tuple[list[dict], str]:
    if not items:
        return items, "NO_ITEMS"

    q = (part_text or "").lower().strip()
    if not q:
        return items, "NO_QUERY"

    text = " ".join(q.split())

    front_tokens = {"перед", "передний", "передняя", "передние", "front"}
    rear_tokens = {"зад", "задний", "задняя", "задние", "rear"}

    wants_front = any(t in text for t in front_tokens)
    wants_rear = any(t in text for t in rear_tokens)

    out = []
    for item in items:
        hay = _item_text(item)
        if wants_front and ("rear" in hay or "зад" in hay):
            continue
        if wants_rear and ("front" in hay or "перед" in hay):
            continue
        out.append(item)

    if out:
        return out, "FILTER_OK"
    return items, "FILTERED_OUT"

