# ── Retry для временных сбоев getPartsbyVIN ──
_PARTS_MAX_RETRIES = 2       # не более 2 повторов (1 основная попытка + 2)
_PARTS_RETRY_PAUSE = 1.0     # пауза между попытками, сек (мягкая, чтобы не добивать лимит)
_NO_RETRY_ERROR_CODES = {"RATE_LIMIT", "AUTH"}   # лимит/авторизацию НЕ ретраим


def _is_transient_parts_error(result: dict) -> bool:
    """True, если ERR временный и имеет смысл повторить запрос.

    Временные: TimeoutError, сетевые aiohttp.ClientError, HTTP 5xx, пустое тело.
    НЕ временные: RATE_LIMIT, AUTH и валидные NO/OK.
    """
    if not isinstance(result, dict) or result.get("api_status") != "ERR":
        return False
    ec = str(result.get("error_code") or "")
    if ec in _NO_RETRY_ERROR_CODES:
        return False
    http = result.get("http_status")
    try:
        if http is not None and int(http) >= 500:
            return True
    except (TypeError, ValueError):
        pass
    if ec in ("TimeoutError", "ServerTimeoutError", "SERVER_ERROR", "NULL_BODY"):
        return True
    ec_l = ec.lower()
    if "timeout" in ec_l or "client" in ec_l or "connection" in ec_l:
        return True
    return False


async def api_get_parts_by_vin(
    session,
    vin: str,
    cat: str,
    *,
    timeout_sec: float = 20.0,
    use_cache: bool = True,
    max_retries: int | None = None,
) -> dict:
    """HTTP-запрос к partsapi getPartsbyVIN.

    Результат всегда проходит через normalize_getparts_response() и
    возвращается как нормализованный dict. Функция никогда не бросает
    исключения наружу. ERR-ответы не кешируются.

    max_retries — сколько повторов делать на ВРЕМЕННЫЕ сбои (timeout/5xx/сеть).
    None -> _PARTS_MAX_RETRIES (для /debug parts). 0 -> без повторов
    (для /vin и coverage, где устойчивость даёт перебор cat, а не повтор).
    Таймаут на повторах эскалируется (+5с/попытку), чтобы дать «медленным»
    cat шанс ответить.
    """
    cache_key = f"parts:{vin}:{cat}"

    if use_cache:
        cached = cache_get(cache_key)
        if cached is not None:
            print(f"[CACHE HIT] getPartsbyVIN {vin} cat={cat}")

            # Нормализованный результат из кеша: пере-извлекаем items из raw,
            # чтобы применить актуальную логику парсинга и не доверять
            # возможно устаревшей структуре items старого кеша.
            if isinstance(cached, dict) and "api_status" in cached:
                raw_cached = cached.get("raw")
                if raw_cached is not None:
                    return normalize_getparts_response(
                        raw_cached,
                        http_status=cached.get("http_status") or 200,
                        cat=cat,
                        vin=vin,
                    )
                return {**cached, "semantic_status": "NOT_APPLICABLE"}

            # Легаси-кеш: сырое тело API (list/dict) -> нормализуем заново
            return normalize_getparts_response(
                cached,
                http_status=200,
                cat=cat,
                vin=vin,
            )

    params = {
        "method": "getPartsbyVIN",
        "key": PARTSAPI_KEY_VIN,
        "vin": vin,
        "type": "oem",
        "cat": cat,
    }

    async def _attempt(attempt_timeout: float) -> dict:
        """Одна попытка запроса -> нормализованный dict (без исключений наружу)."""
        http_status = None
        raw_text = None
        try:
            if API_DELAY > 0:
                await asyncio.sleep(API_DELAY)

            async with session.get(
                BASE_URL,
                params=params,
                timeout=aiohttp.ClientTimeout(total=attempt_timeout),
            ) as r:
                http_status = r.status
                raw_text = await r.text()

                try:
                    data = json.loads(raw_text)
                except Exception:
                    return {
                        "ok": False,
                        "is_error": True,
                        "is_empty": False,
                        "api_status": "ERR",
                        "semantic_status": "NOT_APPLICABLE",
                        "http_status": http_status,
                        "status": http_status or "JSON_ERROR",
                        "error_code": "JSON_ERROR",
                        "message": raw_text[:300] if raw_text else "invalid json",
                        "total": 0,
                        "items": [],
                        "items_raw": [],
                        "total_raw": 0,
                        "raw": raw_text,
                    }

            return normalize_getparts_response(data, http_status=http_status, cat=cat, vin=vin)

        except asyncio.TimeoutError as e:
            return normalize_getparts_response(None, None, exception=e, cat=cat, vin=vin)
        except aiohttp.ClientResponseError as e:
            return normalize_getparts_response(None, e.status, exception=e, cat=cat, vin=vin)
        except aiohttp.ClientError as e:
            return normalize_getparts_response(None, None, exception=e, cat=cat, vin=vin)
        except Exception as e:
            logger.exception("[VIN=%s cat=%s] unexpected error in api_get_parts_by_vin", vin, cat)
            return normalize_getparts_response(None, None, exception=e, cat=cat, vin=vin)

    # Основная попытка + до retries повторов на ВРЕМЕННЫЕ сбои
    retries = _PARTS_MAX_RETRIES if max_retries is None else max(0, int(max_retries))
    attempts = 0
    result: dict = {}
    for attempt in range(1, retries + 2):
        attempts = attempt
        attempt_timeout = timeout_sec + (attempt - 1) * 5  # эскалация: 20, 25, 30…
        result = await _attempt(attempt_timeout)

        if not _is_transient_parts_error(result):
            break  # успех, валидный NO, либо «не временная» ошибка (RATE_LIMIT/AUTH)

        if attempt <= retries:
            logger.warning(
                "[VIN=%s cat=%s] временная ошибка getPartsbyVIN (%s, http=%s) — "
                "повтор %d/%d через %.1fs (следующий таймаут %.0fs)",
                vin, cat, result.get("error_code"), result.get("http_status"),
                attempt, retries, _PARTS_RETRY_PAUSE, timeout_sec + attempt * 5,
            )
            await asyncio.sleep(_PARTS_RETRY_PAUSE)

    # Метаданные о попытках (доп. ключи не влияют на raw_ok/precise_oem/noisy и /vin)
    if isinstance(result, dict):
        result["_attempts"] = attempts
        result["_retried"] = attempts > 1

    # Кешируем только успешные/валидно-пустые ответы (ERR не кешируем)
    if use_cache and isinstance(result, dict) and result.get("api_status") != "ERR":
        cache_set(cache_key, result, allow_err=False)

    return result



async def fetch_parts_with_cat_fallback(
    session: aiohttp.ClientSession,
    vin: str,
    cat: str,
    position: str | None = None,
) -> tuple[str, dict]:
    cats_to_try = [cat] + ALTERNATIVE_CATS.get(cat, [])

    first_err = None
    first_no = None

    for try_cat in cats_to_try:
        if not is_position_safe(cat, try_cat, position):
            continue

        # max_retries=0: устойчивость к таймауту даёт перебор альтернативных
        # cat ниже, а не повтор того же запроса (иначе /vin ждал бы минуты).
        result = await api_get_parts_by_vin(session, vin, try_cat, max_retries=0)

        logger.info(
            "[PARTSAPI] vin=%s cat=%s api_status=%s semantic_status=%s status=%s total=%s msg=%s",
            vin,
            try_cat,
            result.get("api_status"),
            result.get("semantic_status"),
            result.get("status"),
            result.get("total"),
            (result.get("message") or "")[:120],
        )

        api_status = result.get("api_status")

        if api_status == "OK":
            return try_cat, result

        if api_status == "NO":
            if first_no is None:
                first_no = (try_cat, result)
            continue

        # api_status == "ERR"
        if first_err is None:
            first_err = (try_cat, result)

        # Глобальные ошибки (лимит/авторизация) — прекращаем сразу, чтобы не
        # добивать ключ/IP. Временный таймаут/5xx — пробуем следующий cat.
        if str(result.get("error_code")) in _NO_RETRY_ERROR_CODES:
            break
        continue

    if first_err is not None:
        return first_err

    if first_no is not None:
        return first_no

    return cat, {
        "ok": False,
        "is_error": False,
        "is_empty": True,
        "api_status": "NO",
        "semantic_status": "NOT_APPLICABLE",
        "status": 200,
        "http_status": 200,
        "error_code": None,
        "message": "no data after fallback",
        "total": 0,
        "items": [],
        "items_raw": [],
        "total_raw": 0,
        "raw": None,
    }

