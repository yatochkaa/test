def brand_root(brand: str) -> str:
    b = brand.upper()
    for suffix in ("FILTERS", "FILTER", "AUTO", "PARTS", "GROUP"):
        if b.endswith(suffix) and len(b) > len(suffix) + 2:
            b = b[: -len(suffix)].strip()
    return b

