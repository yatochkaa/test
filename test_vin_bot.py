# ── Константы семантического фильтра (перёд/зад) ──
_REAR_STOP_RE = re.compile(
    r"\b(rear|задн|parking|стояноч|shoe|drum|барабан)\b",
    re.IGNORECASE | re.UNICODE,
)

_FRONT_STOP_RE = re.compile(
    r"\b(front|передн)\b",
    re.IGNORECASE | re.UNICODE,
)

_FRONT_CATS = {"281", "82", "1041", "188", "273"}
_REAR_CATS  = {"282", "84", "1042", "189", "274"}

_KNOWN_REAR_ARTICLES = {"4800A001", "MN161157"}


def filter_items_by_query_intent(result: dict, cat: str, position: str | None = None) -> dict:
    """
    Принимает нормализованный result dict, применяет семантический фильтр.
    Возвращает НОВЫЙ dict с обновлёнными полями items / total / semantic_status.
    """
    if not isinstance(result, dict):
        return {
            "api_status": "ERR",
            "semantic_status": "NOT_APPLICABLE",
            "http_status": None,
            "error_code": "BAD_RESULT",
            "message": "filter_items_by_query_intent получил не-dict result",
            "items": [],
            "items_raw": [],
            "total": 0,
            "total_raw": 0,
            "raw": result,
        }

    if result.get("api_status") != "OK":
        return {**result, "semantic_status": "NOT_APPLICABLE"}

    items_raw = result.get("items_raw", [])

    if cat not in _FRONT_CATS and cat not in _REAR_CATS:
        return {**result, "semantic_status": "NOT_APPLICABLE"}

    is_front = cat in _FRONT_CATS
    filtered = []

    for item in items_raw:
        text = _item_text(item)
        article = str(item.get("article", "")).upper().replace("-", "").replace(" ", "")
        blocked = False

        if is_front:
            if article in _KNOWN_REAR_ARTICLES:
                logger.info(
                    "filter: отброшен %s %s — known rear article (cat=%s, pos=%s)",
                    item.get("brand", ""), item.get("article", ""), cat, position,
                )
                blocked = True
            elif _REAR_STOP_RE.search(text):
                logger.info(
                    "filter: отброшен %s %s — rear keyword в тексте %r (cat=%s)",
                    item.get("brand", ""), item.get("article", ""), text[:80], cat,
                )
                blocked = True
        else:
            if _FRONT_STOP_RE.search(text):
                logger.info(
                    "filter: отброшен %s %s — front keyword (cat=%s, pos=%s)",
                    item.get("brand", ""), item.get("article", ""), cat, position,
                )
                blocked = True

        if not blocked:
            filtered.append(item)

    total_raw = len(items_raw)
    total_after = len(filtered)

    if total_raw > 0 and total_after == 0:
        sem_status = "FILTERED_OUT"
        logger.warning(
            "[cat=%s pos=%s] semantic filter: было %d, осталось 0 — FILTERED_OUT",
            cat, position, total_raw,
        )
    else:
        sem_status = "MATCH"

    return {
        **result,
        "items": filtered,
        "total": total_after,
        "semantic_status": sem_status,
    }

