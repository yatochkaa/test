def _build_help_text() -> str:
    # Группируем уникальные part_name по группам — без дублей синонимов
    by_group: dict[int, list[str]] = {}
    seen_per_group: dict[int, set] = {}
    for _kw, (_cats, part_name, group) in PARTS_MAP.items():
        seen = seen_per_group.setdefault(group, set())
        if part_name in seen:
            continue
        seen.add(part_name)
        by_group.setdefault(group, []).append(part_name)

    blocks = ["📦 <b>Доступные запчасти</b>\n"]
    for gid in sorted(GROUP_NAMES.keys()):
        if gid not in by_group:
            continue
        emoji, title = GROUP_NAMES[gid]
        names = ", ".join(by_group[gid])
        blocks.append(f"{emoji} <b>{title}:</b>\n  {names}")

    blocks.append("")
    blocks.append("📋 <b>Использование:</b>")
    blocks.append("  <code>/vin VIN запчасть</code>")
    blocks.append("  Пример: <code>/vin XW7BF4FK60S145161 воздушный фильтр</code>")
    blocks.append("")
    blocks.append("ℹ️ Для тормозных колодок, дисков, амортизаторов,")
    blocks.append("    пружин и рычагов поддержано указание <b>передние/задние</b>.")
    blocks.append("    Без уточнения покажу оба варианта.")
    return "\n".join(blocks)


async def cmd_help(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(_build_help_text(), parse_mode="HTML")

