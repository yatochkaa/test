async def build_coverage_report(
    session: aiohttp.ClientSession,
    vin: str,
    cats: list[tuple[str, str]] | None = None,
) -> dict:
    if cats is None:
        cats = CURATED_DEBUG_CATS

    # Марка авто определяется один раз — нужна для оценки «точного OEM».
    manu_name = ""
    if PARTSAPI_KEY_VINDECODE and os.getenv("USE_VINDECODE_IN_VIN", "0") == "1":
        vin_info = await api_vindecode(session, vin)
        if vin_info:
            manu_name = vin_info.get("manuName", "") or ""

    total_cats = len(cats)
    per_cat = []
    ok_list = []
    no_list = []
    weak_list = []
    err_list = []
    stopped = False
    stopped_reason = None

    for cat_id, cat_name in cats:
        result = await api_get_parts_by_vin(session, vin, str(cat_id), use_cache=False, max_retries=0)
        result = filter_items_by_query_intent(result, str(cat_id), None)

        api_status = result.get("api_status", "ERR")
        total = result.get("total", 0)
        error_code = result.get("error_code")

        # Качество относительно марки авто: OK / NO / WEAK / ERR
        quality, q = coverage_quality(result, manu_name)

        samples = []
        for item in result.get("items", []):
            brand = (item.get("brand") or "").strip()
            art = (item.get("article") or "").strip()
            if brand and art:
                samples.append((brand, art))
            if len(samples) >= 2:
                break

        row = {
            "catid": str(cat_id),
            "cat_id": str(cat_id),
            "name": cat_name,
            "api_status": api_status,
            "semantic_status": result.get("semantic_status"),
            "quality": quality,
            "raw_ok": q.get("raw_ok", False),
            "precise_oem": q.get("precise_oem", False),
            "noisy": q.get("noisy", False),
            "count": total,
            "samples": samples,
            "error_code": error_code,
            "message": result.get("message"),
        }
        per_cat.append(row)

        if quality == "OK":
            ok_list.append(row)
        elif quality == "WEAK":
            weak_list.append(row)
        elif quality == "NO":
            no_list.append(row)
        else:
            err_list.append(row)

        # Защита от rate-limit: при глобальной ошибке API прекращаем перебор,
        # чтобы не добивать лимит ключа/IP.
        if api_status == "ERR" and str(error_code) in _STOP_ON_ERROR_CODES:
            stopped = True
            stopped_reason = str(error_code)
            logger.warning(
                "build_coverage_report: остановка на cat=%s из-за %s (%s)",
                cat_id, error_code, (result.get("message") or "")[:120],
            )
            break

        if API_DELAY > 0:
            await asyncio.sleep(API_DELAY)

    checked = len(per_cat)
    ok_count = len(ok_list)
    weak_count = len(weak_list)

    if err_list:
        level = "error"
    elif checked >= 15 and ok_count >= 8:
        level = "ok"
    elif ok_count >= 3:
        level = "partial"
    elif ok_count + weak_count >= 3:
        level = "weak"
    else:
        level = "weak"

    return {
        "level": level,
        "manu_name": manu_name,
        "per_cat": per_cat,
        "ok_list": ok_list,
        "no_list": no_list,
        "weak_list": weak_list,
        "err_list": err_list,
        "checked": checked,
        "total_cats": total_cats,
        "stopped": stopped,
        "stopped_reason": stopped_reason,
    }

