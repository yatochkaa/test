async def async_get(session: aiohttp.ClientSession, params: dict, timeout: int = 25):
    # Защита от rate-limit: пауза перед запросом
    if API_DELAY > 0:
        await asyncio.sleep(API_DELAY)
    try:
        async with session.get(
            BASE_URL,
            params=params,
            timeout=aiohttp.ClientTimeout(total=timeout),
        ) as r:
            text = await r.text()
            print(f"HTTP {r.status} | {params.get('method')} | {text[:300]}")
            return await r.json(content_type=None)
    except (asyncio.TimeoutError, aiohttp.ServerTimeoutError) as e:
        print(f"TIMEOUT in {params.get('method')} (cat={params.get('cat')}): {timeout}s")
        logger.warning("API %s timeout (%ss): %s", params.get("method"), timeout, e)
        return None
    except Exception as e:
        print(f"EXCEPTION in {params.get('method')}: {type(e).__name__}: {e}")
        logger.error("API %s error: %s", params.get("method"), e)
        return None

